void main() {
  String firstName = 'Muhammad';
  String middleName = 'Adnan';
  String lastName = 'Husien';

  print(firstName);
  print(middleName);
  print(lastName);

  //expression
  String firstName = 'Muhammad';
  String middleName = 'Adnan';
  String lastName = "Husien";

  var fullName = '$firstName  $middleName  ${lastName}';

  print(fullName);

  //karakter Backslash
  var text = 'this is \'dart\' \$cool';
  print(text);

  //menggabungkan string
  var name1 = firstName + middleName + lastName;
  var name2 = 'Muhammad' 'Adnan' 'Husien';

  print(name1);
  print(name2);

  //multiline string
  var longString = ;
  print(longString);
}